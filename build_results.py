#!/usr/bin/env python3
"""
Export the Gemma manuscript summaries into maintainable, human-readable results.

Reads papers/manuscript_summaries.json (the combined Pydantic output) and writes,
into a tracked results/ folder:

  results/SUMMARY.md      — executive summary (counts, EHR-based papers,
                            top pathologies, top captured features)
  results/checklist.md    — readable table of all 116 per-paper checklists
  results/manuscript_summaries.json — copy of the combined JSON (tracked)

Run: python build_results.py    (or `make results`)
"""
from __future__ import annotations

import json
import shutil
from collections import Counter
from pathlib import Path

COMBINED = Path("papers/manuscript_summaries.json")
OUT_DIR = Path("results")


def load() -> list[dict]:
    data = json.loads(COMBINED.read_text())
    return data.get("summaries", [])


def top(counter: Counter, n: int = 15) -> list[tuple[str, int]]:
    return counter.most_common(n)


def _clip(text: str, width: int) -> str:
    """Truncate at a word boundary (never mid-word) and escape table pipes.

    Avoids ugly chops like 'carbon monoxi…'; instead yields 'carbon…'.
    """
    s = (text or "").replace("|", "/").strip()
    if len(s) <= width:
        return s or "—"
    cut = s[:width]
    # back up to the last space so we don't split a word
    if " " in cut:
        cut = cut.rsplit(" ", 1)[0].rstrip(" ,;:.-")
    return cut + "…"


def main() -> None:
    summaries = load()
    OUT_DIR.mkdir(exist_ok=True)

    # track a copy of the combined JSON alongside the readable exports
    shutil.copyfile(COMBINED, OUT_DIR / "manuscript_summaries.json")

    n = len(summaries)
    ehr_papers = [s for s in summaries if s.get("ehr_used")]
    n_ehr = len(ehr_papers)
    years = sorted({s.get("year", "?") for s in summaries})

    diseases = Counter()
    features = Counter()
    designs = Counter()
    sources = Counter()
    exposures = Counter()
    confidences = Counter()
    for s in summaries:
        for d in s.get("pathologies_diseases", []) or []:
            diseases[d.lower().strip()] += 1
        for f in s.get("captured_features", []) or []:
            features[f.lower().strip()] += 1
        designs[(s.get("study_design") or "").strip() or "unspecified"] += 1
        sources[(s.get("data_source_type") or "").strip() or "unspecified"] += 1
        exposures[(s.get("exposure_domain") or "").strip() or "unspecified"] += 1
        confidences[(s.get("confidence") or "unclear").strip()] += 1

    # ── SUMMARY.md ───────────────────────────────────────────────────────
    L = []
    L.append("# Manuscript Summarization Results")
    L.append("")
    L.append("> Generated by `build_results.py` from the Gemma 4 12B Pydantic checklists.")
    L.append(f"> Source: `papers/manuscript_summaries.json` · {n} papers · span {years[0]}–{years[-1]}.")
    L.append("")
    L.append("## Headline")
    L.append("")
    L.append(f"- **{n} manuscripts** summarized with Gemma 4 12B and validated against the Pydantic `ManuscriptChecklist` schema.")
    L.append(f"- **{n_ehr} / {n} ({100*n_ehr//n}%)** identified as using EHR / claims / linked administrative health data.")
    L.append(f"- Per-paper JSON: `papers/summaries/<pmcid>.json` · Combined: `results/manuscript_summaries.json`.")
    L.append("")
    L.append("## EHR-based papers")
    L.append("")
    L.append("| PMCID | Year | Disease(s) | Data source | Title |")
    L.append("|-------|------|-----------|-------------|-------|")
    for s in sorted(ehr_papers, key=lambda x: (x.get("year", ""), x["pmcid"])):
        dis = "; ".join(s.get("pathologies_diseases", []) or ["—"])
        src = (s.get("data_source_type") or "—")
        L.append(f"| {s['pmcid']} | {s.get('year','')} | {dis[:60]} | {src[:30]} | {_clip(s['title'], 70)} |")
    L.append("")
    L.append("## Top pathologies / diseases")
    L.append("")
    L.append("| Disease | Papers |")
    L.append("|----------|--------|")
    for d, c in top(diseases, 20):
        L.append(f"| {d} | {c} |")
    L.append("")
    L.append("## Top captured EHR features / variables")
    L.append("")
    L.append("| Feature | Papers |")
    L.append("|---------|--------|")
    for f, c in top(features, 20):
        L.append(f"| {f} | {c} |")
    L.append("")
    L.append("## Confidence distribution")
    L.append("")
    L.append("| Confidence | Papers |")
    L.append("|------------|--------|")
    for conf in ["high", "medium", "low", "unclear"]:
        L.append(f"| {conf} | {confidences.get(conf, 0)} |")
    L.append("")
    L.append("## Data source types")
    L.append("")
    L.append("| Data source | Papers |")
    L.append("|------------|--------|")
    for s, c in top(sources, 12):
        L.append(f"| {s[:40]} | {c} |")
    L.append("")
    (OUT_DIR / "SUMMARY.md").write_text("\n".join(L) + "\n")

    # ── checklist.md (all papers) ─────────────────────────────────────────
    C = []
    C.append("# Full Manuscript Checklist")
    C.append("")
    C.append(f"All {n} summarized papers. See `SUMMARY.md` for the executive summary.")
    C.append("")
    C.append("| # | PMCID | Year | EHR? | Disease(s) | Data source | Summary |")
    C.append("|---|-------|------|------|-----------|-------------|---------|")
    for i, s in enumerate(sorted(summaries, key=lambda x: (x.get("year", ""), x["pmcid"])), 1):
        dis = "; ".join((s.get("pathologies_diseases") or [])[:3]) or "—"
        C.append(
            f"| {i} | {s['pmcid']} | {s.get('year','')} | "
            f"{'✓' if s.get('ehr_used') else ''} | {_clip(dis, 50)} | "
            f"{_clip(s.get('data_source_type') or '—', 30)} | "
            f"{_clip(s.get('summary') or '', 110)} |"
        )
    C.append("")
    (OUT_DIR / "checklist.md").write_text("\n".join(C) + "\n")

    print(f"✓ wrote {OUT_DIR}/SUMMARY.md, {OUT_DIR}/checklist.md, "
          f"{OUT_DIR}/manuscript_summaries.json")
    print(f"  {n} papers | {n_ehr} EHR-based | {years[0]}–{years[-1]}")


if __name__ == "__main__":
    main()
